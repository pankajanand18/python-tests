class SinglyLinkedListNode(object):

    def __init__(self,data):
        self.data= data
        self.next=None


    def addAfter(self,node):
        node.next = self











class DoublyLinkedListNode(object):
    def __init__(self):
        self.data = None
        self.next = None
        self.prev= None

    def __init__(self,data):
        self.data = data
        self.prev = None
        self.next = None









